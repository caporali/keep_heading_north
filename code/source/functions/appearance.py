import sys
import time

def tab(n = 4):
	"""
	description:
		Print a tab.
	syntax:
		tab()
	"""
	for _ in range(n):
		sys.stdout.write(" ")
		sys.stdout.flush()

def slow_print(text, newline = "\n"):
	"""
	description:
		Print the text one character at a time.
	syntax:
		slow_print(text)
	"""
	text += newline
	for character in text:
		sys.stdout.write(character)
		sys.stdout.flush()
		time.sleep(0.05)
  
def slow_input(text):
	"""
	description:
		Print the text related to an input one character at a time.
	syntax:
		input = slow_input(text)
	"""
	for character in text:
		sys.stdout.write(character)
		sys.stdout.flush()
		time.sleep(0.05)
	value = input()  
	return value

